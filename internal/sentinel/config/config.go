func (c *Config) anyCollectorEnabled() bool {
	return c.RPC.Enabled || c.Logs.Enabled || c.OTLP.Enabled || c.Resources.Enabled || c.Metadata.Enabled || c.Self.Enabled
}

// isNoiseScheme returns true when the server URL uses noise://, i.e. when
// traffic is routed through a beacon instead of directly to the watchtower.
func (c *Config) isNoiseScheme() bool {
	return strings.HasPrefix(c.Server.URL, "noise://")
}

// validateBeacon enforces the coupling between server.url scheme and
// [beacon]: noise:// requires keys_dir; public_key requires noise://.
func (c *Config) validateBeacon() error {
	noise := c.isNoiseScheme()
	if noise && c.Beacon.KeysDir == "" {
		return fmt.Errorf("beacon.keys_dir is required when server.url uses noise://")
	}
	if !noise && c.Beacon.KeysDir != "" {
		return fmt.Errorf("beacon.keys_dir is only meaningful when server.url uses noise://")
	}
	if !noise && c.Beacon.PublicKey != "" {
		return fmt.Errorf("beacon.public_key is only meaningful when server.url uses noise://")
	}
	return nil
}

// NoiseConfig builds a pkg/noise.Config from the beacon section, loading the
// static keypair from disk. Returns nil when the URL scheme isn't noise://
// (callers pass the nil through to sender.New, which falls back to HTTPS).
func (c *Config) NoiseConfig() (*noisepkg.Config, error) {
	if !c.isNoiseScheme() {
		return nil, nil
	}
	kp, err := noisepkg.LoadKeypair(c.Beacon.KeysDir)
	if err != nil {
		return nil, fmt.Errorf("load beacon keypair: %w", err)
	}
	cfg := &noisepkg.Config{Static: kp}
	if c.Beacon.PublicKey != "" {
		pub, err := noisepkg.DecodePublicKey(c.Beacon.PublicKey)
		if err != nil {
			return nil, fmt.Errorf("beacon.public_key: %w", err)
		}
		cfg.AuthorizedKeys = [][]byte{pub}
	}
	return cfg, nil
}

func (c *Config) validate() error {
	if c.anyCollectorEnabled() {
		if c.Server.URL == "" {
			return fmt.Errorf("server.url is required when a collector is enabled")
		}
		if c.Server.Token == "" {
			return fmt.Errorf("server.token is required when a collector is enabled")
		}
	}
	if err := c.validateBeacon(); err != nil {
		return err
	}
	if c.Health.Enabled && c.Health.ListenAddr == "" {
		return fmt.Errorf("health.listen_addr is required when health is enabled")
	}
	if c.Metadata.Enabled && c.Metadata.ConfigPath != "" && c.Metadata.ConfigGetCmd != "" {
		return fmt.Errorf("metadata: set exactly one of config_path or config_get_cmd, not both")
	}
	if c.Logs.Enabled {
		validLogSources := []string{LogSourceDocker, LogSourceJournald}
		if !slices.Contains(validLogSources, c.Logs.Source) {
			return fmt.Errorf("logs.source %q: must be one of %v", c.Logs.Source, validLogSources)
		}
		if !slices.Contains(validLogLevels, c.Logs.MinLevel) {
			return fmt.Errorf("logs.min_level %q: must be one of %v", c.Logs.MinLevel, validLogLevels)
		}
	}
	if c.Resources.Enabled {
		validResSources := []string{ResSourceHost, ResSourceDocker, ResSourceBoth}
		if !slices.Contains(validResSources, c.Resources.Source) {
			return fmt.Errorf("resources.source %q: must be one of %v", c.Resources.Source, validResSources)
		}
	}
	return nil
}
